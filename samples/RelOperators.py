x = input("Enter a number: ");
y = input("Enter another number: ");
print ("x = ", x);
print ("y = ", y);

# Greather than
print ("x > y: ", x > y);

# Less than
print ("x < y: ", x < y);

# Greather or Equal
print("x >= y: ", x >= y);

# Less or Equal
print ("x <= y: ", x <= y);

# Not Equal
print ("x != y: ", x != y);

# Equal
print ("x == y: ", x == y);
